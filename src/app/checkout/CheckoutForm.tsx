interface DonatedItem {
  case_id: number;
  institution_name?: string;
  need_id?: string | number;
  acf_field_id: string;
  item_name: string;
  item_quantity: number;
  unit_price: number;
  line_total: number;
}

interface CheckoutFormProps {
  caseId: string;
  totalPaidAmount: number;
  subtotalAmount: number;
  shippingFees: number;
  customDonation: number;
  donatedItems: DonatedItem[];
  donorName?: string;
  donorEmail?: string;
}

const CURRENCY = "usd";

// üö® ÿØÿßŸÑÿ© ŸÖÿ≥ÿßÿπÿØÿ© ŸÑÿ•ÿ∂ÿßŸÅÿ© ÿ™ÿ£ÿÆŸäÿ± (ŸÖŸáŸÖÿ© ŸÑŸÖŸÜÿπ ŸÖÿ¥ÿßŸÉŸÑ ÿ≥ÿ±ÿπÿ© ÿßŸÑÿ™ÿ≠ŸÖŸäŸÑ)
const sleep = (ms: number) => new Promise((resolve) => setTimeout(resolve, ms));

const CheckoutForm: React.FC<CheckoutFormProps> = ({
  totalPaidAmount,
  subtotalAmount,
  shippingFees,
  customDonation,
  donatedItems,
  donorName,
  donorEmail,
}) => {
  const stripe = useStripe();
  const elements = useElements();
  const { data: session } = useSession();
  const { clearCart } = useCart();
  const router = useRouter();

  const [error, setError] = useState<string | null>(null);
  const [processing, setProcessing] = useState(false);
  const [succeeded, setSucceeded] = useState(false);
  const [clientSecret, setClientSecret] = useState<string>("");
  const [paymentRequest, setPaymentRequest] = useState<any | null>(null);

  const effectiveDonorName =
    (session?.user?.name && session.user.name.trim()) ||
    (donorName && donorName.trim()) ||
    "ŸÅÿßÿπŸÑ ÿÆŸäÿ±";

  const effectiveDonorEmail =
    (session?.user?.email && session.user.email.trim()) ||
    (donorEmail && donorEmail.trim()) ||
    "";

  const effectiveUserId =
    Number((session as any)?.user?.wordpressUserId || 0) || 0;

  const totalAmountInCents = Math.round(totalPaidAmount * 100);

  // ------------------------------------------------------------------
  // ‚úÖ 1. ÿØÿßŸÑÿ© ÿ™ÿ≥ÿ¨ŸäŸÑ ÿßŸÑÿ™ÿ®ÿ±ÿπ ŸÅŸä ŸàŸàÿ±ÿØÿ®ÿ±Ÿäÿ≥ (ÿ™ŸÖ ÿ™ÿµÿ≠Ÿäÿ≠Ÿáÿß ÿ®ÿßŸÑŸÉÿßŸÖŸÑ)
  // ------------------------------------------------------------------
  const submitDonationToWP = useCallback(
    async (paymentIntentId: string) => {
      try {
        const firstItem = donatedItems[0];
        const caseId = firstItem?.case_id || 0;
        const needId = firstItem?.need_id || 0;
        const quantity = firstItem?.item_quantity || 1;

        // ‚úÖ payload Ÿäÿ™ŸàÿßŸÅŸÇ ŸÖÿπ /api/donations
        const payload = {
          amount: totalAmountInCents,
          caseId,
          stripePaymentIntentId: paymentIntentId,
          needId,
          quantity,
        };

        console.log("üì§ Sending donation payload:", payload);

        const response = await fetch("/api/donations", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(payload),
        });

        const data = await response.json().catch(() => ({}));

        if (!response.ok) {
          console.error(
            `‚ö†Ô∏è WP RECORDING WARNING: Stripe succeeded but WP failed (HTTP ${response.status}). Error: ${
              data?.error || "Failed to submit donation"
            }`
          );
          return { success: false, error: "WP recording failed." };
        }

        console.log(
          "‚úÖ WP RECORDING SUCCESS: Donation recorded successfully in WordPress."
        );
        return { success: true };
      } catch (err: any) {
        console.error("‚ùå WP RECORDING FATAL ERROR:", err);
        return { success: false, error: err.message };
      }
    },
    [totalAmountInCents, donatedItems]
  );

  // ------------------------------------------------------------------
  // 2. ÿ™ŸáŸäÿ¶ÿ© ÿßŸÑÿØŸÅÿπ (Card + Express)
  // ------------------------------------------------------------------
  const fetchPaymentIntentAndSetupExpress = useCallback(
    async (): Promise<(() => void) | undefined> => {
      if (totalAmountInCents <= 0 || !stripe) {
        setClientSecret("");
        setPaymentRequest(null);
        return;
      }

      setError(null);
      let secret = "";

      try {
        const body = {
          amount: totalAmountInCents,
          subtotal_amount: Math.round(subtotalAmount * 100),
          shipping_fees: Math.round(shippingFees * 100),
          custom_donation: Math.round(customDonation * 100),
          case_ids: [
            ...new Set(
              donatedItems.map((i) => Number(i.case_id)).filter(Boolean)
            ),
          ],
          donated_items: JSON.stringify(donatedItems),
          donor_name: effectiveDonorName,
          donor_email: effectiveDonorEmail,
          user_id: effectiveUserId,
        };

        const res = await fetch("/api/create-payment-intent", {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify(body),
        });

        const data = await res.json().catch(() => ({}));

        if (!res.ok || !data.clientSecret) {
          setError(data?.error || "ŸÅÿ¥ŸÑ ÿ™ŸáŸäÿ¶ÿ© ÿßŸÑÿØŸÅÿπ.");
          return;
        }

        secret = data.clientSecret;
        setClientSecret(secret);
      } catch (err) {
        setError("ÿ≠ÿØÿ´ ÿÆÿ∑ÿ£ ÿ£ÿ´ŸÜÿßÿ° ÿ™ŸáŸäÿ¶ÿ© ÿπŸÖŸÑŸäÿ© ÿßŸÑÿØŸÅÿπ. Ÿäÿ±ÿ¨Ÿâ ÿßŸÑŸÖÿ≠ÿßŸàŸÑÿ© ŸÖÿ±ÿ© ÿ£ÿÆÿ±Ÿâ.");
        return;
      }

      // Apple Pay / Google Pay setup
      const pr = stripe.paymentRequest({
        country: "US",
        currency: CURRENCY,
        total: {
          label: "ÿ•ÿ¨ŸÖÿßŸÑŸä ÿßŸÑÿ™ÿ®ÿ±ÿπ",
          amount: totalAmountInCents,
        },
        requestPayerName: true,
        requestPayerEmail: true,
      });

      pr.canMakePayment().then((result) => {
        if (result) {
          setPaymentRequest(pr);
        } else {
          setPaymentRequest(null);
        }
      });

      const handlePaymentMethod = async (event: any) => {
        if (processing) return;

        setProcessing(true);
        setError(null);

        const { error: confirmError, paymentIntent } =
          await stripe!.confirmCardPayment(
            secret,
            { payment_method: event.paymentMethod.id },
            { handleActions: false }
          );

        if (confirmError) {
          event.complete("fail");
          setError(confirmError.message || "ÿ≠ÿØÿ´ ÿÆÿ∑ÿ£ ŸÅŸä ÿ™ÿ£ŸÉŸäÿØ ÿßŸÑÿØŸÅÿπ ÿßŸÑÿ≥ÿ±Ÿäÿπ.");
          setProcessing(false);
        } else if (paymentIntent && paymentIntent.status === "succeeded") {
          event.complete("success");

          setSucceeded(true);
          clearCart();

          await submitDonationToWP(paymentIntent.id);
          router.push(`/thank-you?payment_intent=${paymentIntent.id}`);
          setProcessing(false);
        } else {
          event.complete("fail");
          setError("ÿ™ÿπÿ∞Ÿëÿ± ÿ•ŸÉŸÖÿßŸÑ ÿßŸÑÿØŸÅÿπ ÿßŸÑÿ≥ÿ±Ÿäÿπ.");
          setProcessing(false);
        }
      };

      pr.on("paymentmethod", handlePaymentMethod);
      return () => {
        pr.off("paymentmethod", handlePaymentMethod);
      };
    },
    [
      totalAmountInCents,
      stripe,
      subtotalAmount,
      shippingFees,
      customDonation,
      donatedItems,
      effectiveDonorName,
      effectiveDonorEmail,
      effectiveUserId,
      processing,
      submitDonationToWP,
      clearCart,
      router,
    ]
  );

  // ------------------------------------------------------------------
  // 3. useEffect ŸÑŸÑÿ™ŸÜÿ∏ŸäŸÅ
  // ------------------------------------------------------------------
  useEffect(() => {
    let cleanupFunction: (() => void) | undefined;

    fetchPaymentIntentAndSetupExpress()
      .then((cleanup) => {
        cleanupFunction = cleanup;
      })
      .catch((err) => {
        console.error("Payment setup failed:", err);
      });

    return () => {
      if (cleanupFunction) cleanupFunction();
    };
  }, [fetchPaymentIntentAndSetupExpress]);

  // ------------------------------------------------------------------
  // 4. ÿ•ÿ±ÿ≥ÿßŸÑ ÿßŸÑÿØŸÅÿπ ÿ®ÿßŸÑÿ®ÿ∑ÿßŸÇÿ© ÿßŸÑÿ™ŸÇŸÑŸäÿØŸäÿ©
  // ------------------------------------------------------------------
  const handleSubmit = async (event: React.FormEvent) => {
    event.preventDefault();

    if (
      !stripe ||
      !elements ||
      !clientSecret ||
      processing ||
      succeeded ||
      totalAmountInCents <= 0
    ) {
      return;
    }

    setProcessing(true);
    setError(null);
    const cardNumberElement = elements.getElement(CardNumberElement);
    await sleep(50);

    if (!cardNumberElement) {
      setError("ÿπŸÜÿµÿ± ÿ±ŸÇŸÖ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ© ÿ∫Ÿäÿ± ŸÖŸàÿ¨ŸàÿØ. Ÿäÿ±ÿ¨Ÿâ ÿ™ÿ≠ÿØŸäÿ´ ÿßŸÑÿµŸÅÿ≠ÿ©.");
      setProcessing(false);
      return;
    }

    const { error: confirmError, paymentIntent } =
      await stripe!.confirmCardPayment(clientSecret, {
        payment_method: { card: cardNumberElement },
      });

    if (confirmError) {
      setError(confirmError.message || "ÿ≠ÿØÿ´ ÿÆÿ∑ÿ£ ÿ∫Ÿäÿ± ŸÖÿ™ŸàŸÇÿπ.");
      setProcessing(false);
      return;
    }

    if (!paymentIntent || paymentIntent.status !== "succeeded") {
      setError("ÿ™ÿπÿ∞Ÿëÿ± ÿ•ŸÉŸÖÿßŸÑ ÿßŸÑÿØŸÅÿπ.");
      setProcessing(false);
      return;
    }

    setSucceeded(true);
    setProcessing(false);
    clearCart();

    await submitDonationToWP(paymentIntent.id);
    router.push(`/thank-you?payment_intent=${paymentIntent.id}`);
  };

  // ------------------------------------------------------------------
  // 5. ÿßŸÑÿπÿ±ÿ∂ (Render)
  // ------------------------------------------------------------------
  const elementOptions = {
    style: {
      base: {
        fontSize: "16px",
        color: "#424770",
        "::placeholder": { color: "#aab7c4" },
      },
      invalid: { color: "#9e2146" },
    },
    hidePostalCode: true,
  };

  return (
    <form onSubmit={handleSubmit} className={styles.checkoutForm} dir="rtl">
      <h2 className={styles.formTitle}>ÿ™ŸÅÿßÿµŸäŸÑ ÿßŸÑÿØŸÅÿπ ÿßŸÑÿ¢ŸÖŸÜ</h2>

      {error && <div className={styles.errorMessage}>{error}</div>}
      {!clientSecret && !error && !succeeded && totalPaidAmount > 0 && (
        <div className={styles.loadingMessage}>ÿ¨ÿßÿ±Ÿä ÿ•ÿπÿØÿßÿØ ÿßŸÑÿØŸÅÿπ...</div>
      )}
      {totalPaidAmount <= 0 && (
        <div className={styles.errorMessage}>
          ŸÑÿß ŸäŸÖŸÉŸÜ ÿßŸÑŸÖÿ™ÿßÿ®ÿπÿ©ÿå ÿßŸÑŸÖÿ®ŸÑÿ∫ ÿßŸÑÿ•ÿ¨ŸÖÿßŸÑŸä ÿµŸÅÿ±.
        </div>
      )}

      {clientSecret && totalPaidAmount > 0 && !succeeded && (
        <>
          {paymentRequest && (
            <div className={styles.expressCheckoutSection}>
              <p className={styles.expressTitle}>ÿßŸÑÿØŸÅÿπ ÿßŸÑÿ≥ÿ±Ÿäÿπ:</p>
              <PaymentRequestButtonElement
                options={{
                  paymentRequest: paymentRequest,
                  style: {
                    paymentRequestButton: {
                      type: "donate",
                      theme: "dark",
                      height: "56px",
                    },
                  },
                }}
              />
              <div className={styles.divider}>
                <span>ÿ£Ÿà ÿßŸÑÿØŸÅÿπ ÿ®ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©</span>
              </div>
            </div>
          )}

          <div className={styles.cardInputGroup}>
            <div className={styles.formGroup}>
              <label className={styles.cardLabel}>ÿ±ŸÇŸÖ ÿßŸÑÿ®ÿ∑ÿßŸÇÿ©</label>
              <div className={styles.cardInputFieldContainer}>
                <CardNumberElement options={elementOptions} />
              </div>
            </div>

            <div className={styles.cardSplitRow}>
              <div className={styles.formGroup}>
                <label className={styles.cardLabel}>ÿ™ÿßÿ±ŸäÿÆ ÿßŸÑÿßŸÜÿ™Ÿáÿßÿ°</label>
                <div className={styles.cardInputFieldContainer}>
                  <CardExpiryElement options={elementOptions} />
                </div>
              </div>
              <div className={styles.formGroup}>
                <label className={styles.cardLabel}>ÿ±ŸÖÿ≤ CVC</label>
                <div className={styles.cardInputFieldContainer}>
                  <CardCvcElement options={elementOptions} />
                </div>
              </div>
            </div>
          </div>
        </>
      )}

      {succeeded && (
        <div className={styles.successMessage}>
          <h2 style={{ color: "#28a745" }}>‚úÖ ÿ¥ŸÉÿ±ÿßŸã ŸÑŸÉ!</h2>
          <p>ÿ™ŸÖ ÿßÿ≥ÿ™ŸÑÿßŸÖ ÿ™ÿ®ÿ±ÿπŸÉ ÿ®ŸÜÿ¨ÿßÿ≠ÿå Ÿàÿ¨ÿßÿ±Ÿä ÿ™ÿ¨ŸáŸäÿ≤ ÿµŸÅÿ≠ÿ© ÿßŸÑÿ™ÿ£ŸÉŸäÿØ.</p>
        </div>
      )}

      {!succeeded && (
        <button
          type="submit"
          disabled={
            processing ||
            !stripe ||
            !clientSecret ||
            !!error ||
            totalAmountInCents <= 0
          }
          className={styles.submitButton}
        >
          {processing
            ? "ÿ¨ÿßÿ±Ÿä ÿßŸÑŸÖÿπÿßŸÑÿ¨ÿ©..."
            : `ÿ™ÿ®ÿ±ÿπ ÿßŸÑÿ¢ŸÜ (${totalPaidAmount.toFixed(2)}$)`}
        </button>
      )}

      <p className={styles.secureNote}>
        ÿπŸÖŸÑŸäÿ© ÿØŸÅÿπ ÿ¢ŸÖŸÜÿ© ŸàŸÖÿ¥ŸÅÿ±ÿ© ÿ®Ÿàÿßÿ≥ÿ∑ÿ© Stripe.
      </p>
    </form>
  );
};

